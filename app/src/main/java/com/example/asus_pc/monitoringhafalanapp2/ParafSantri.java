public class ParafSantri {
    private String nomorParaf;
    private String namaSurah;
    private String selesai;
    private String tanggalParaf;
    private String namaPemaraf;
    private String catatan;

    public String getNomorParaf() {
        return nomorParaf;
    }

    public void setNomorParaf(String nomorParaf) {
        this.nomorParaf = nomorParaf;
    }

    public String getNamaSurah() {
        return namaSurah;
    }

    public void setNamaSurah(String namaSurah) {
        this.namaSurah = namaSurah;
    }

    public String getSelesai() {
        return selesai;
    }

    public void setSelesai(String selesai) {
        this.selesai = selesai;
    }

    public String getTanggalParaf() {
        return tanggalParaf;
    }

    public void setTanggalParaf(String tanggalParaf) {
        this.tanggalParaf = tanggalParaf;
    }

    public String getNamaPemaraf() {
        return namaPemaraf;
    }

    public void setNamaPemaraf(String namaPemaraf) {
        this.namaPemaraf = namaPemaraf;
    }

    public String getCatatan() {
        return catatan;
    }

    public void setCatatan(String catatan) {
        this.catatan = catatan;
    }
}
